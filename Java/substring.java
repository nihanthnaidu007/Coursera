public class substring {
    public static void main(String args[])
    {
        String s1="Nihanth Naidu";
        String s2="Nihanth Naidu";

        String n1=s1.substring(3);
        String n2=s2.substring(3,7);

        System.out.println("Sub string of given index is: "+n1);
        System.out.println("Sub string between start and end index is: "+n2);
    }
}
