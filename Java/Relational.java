class Relational
{
    public static void main(String args[])
    {
        byte x,y;
        x=20;
        y=30;
        System.out.println("Result of x>y is"+(x>y));
        System.out.println("Result of x>=y is"+(x>=y));
        System.out.println("Result of x<y is"+(x<y));
        System.out.println("Result of x<=y is"+(x<=y));
        System.out.println("Result of x==y is"+(x==y));
        System.out.println("Result of x!=y is"+(x!=y));
    }
}