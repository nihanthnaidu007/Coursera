class twoarray1
{
    public static void main(String args[]) 
    {
        int marks[][]={{10,6,7,3,5},{5,9,10,9,7},{9,8,6,8,5}};
  
        {

        for(int i=0;i<3;i++)
        {
           for(int j=0;j<5;j++)
           {
            System.out.print(+marks[i][j]+"\t");// \t means tab space
           }
           System.out.println();
        }
    }
    }
}