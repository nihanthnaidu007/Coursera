public class casestring {
    public static void main(String args[])
    {
        String s1="NIHANTH NAIDU";
        String s2="nihanth naidu";

        String n1=s1.toLowerCase();
        String n2=s2.toUpperCase();

        System.out.println("Converted lower case: "+n1);
        System.out.println("Converted upper case: "+n2);
    }
}
